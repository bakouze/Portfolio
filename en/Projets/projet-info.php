    <h4>Présentation du projet</h4>
    <p>En première année d'école d'ingénieur, j'ai aussi participé à un projet informatique par groupe de deux dont le but était d'écrire un programme informatique en Java permettant l'optimisation du remplissage de conteneur dans le cadre du concours ESICUP organié par Renault. Des instances nous étaient fournies avec diférents articles (modélisés par 3 dimensions : hauteur, largeur et longueur; et leur poids) ainsi que différents modèle de conteneurs (plus ou moins grands, haut, large). Le but était de ranger au mieux ses articles dans le moins de conteneur possible afin de minimiser le volume envoyé, en se pliant à plusieurs contraintes définies par le sujet.</p>
