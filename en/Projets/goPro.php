    <h4 text-center>Présentation du projet</h4>
    <p>
      En première année d'école d'ingénieur à Grenoble INP - Génie industriel, j'ai travaillé avec Gaël Spiral et Lara Gaulier sur un projet de conception / informatique industrielle dont le but était de trouver une solution de pour un pied de caméra de type GoPro afin de permettre la rotation en pan et tilt. Nous avons donc décider de créer une application android permettant de contrôler à distance notre pied de caméra en bluetooth par une connexion avec une carte Arduino Uno. Le projet était donc composé de trois parties :
      <ul>
        <li>Conception et réalisation technique du support de caméra</li>
        <li>Création du programme Arduino</li>
        <li>Creation de l'application Android</li>
      </ul>
    </p>
