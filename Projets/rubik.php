    <h4 class="text-center">Présentation du projet</h4>
    <p>
      Dans le cadre du TIPE (Travaux d'Initiative Personnelle Encadrés) de la seconde année de classe préparatoire aux grandes écoles MP, j'ai travaillé sur la résolution informatique du rubik's cube. Le but du projet n'était pas de prouver que la résolution informatique était possible, ni de trouver une méthode optimale de résolution, mais plutôt de voir les problématiques rencontrées lors de la transcription en langage informatique d'une méthode de résolution imaginée pour les être humains : la méthode dite "couche par couche".
    </p>
